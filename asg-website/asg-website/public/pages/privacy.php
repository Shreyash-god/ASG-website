<h1>PRIVACY POLICY</h1>
<div class="meta">LAST UPDATED: JANUARY 2025 · ASG STUDIOS & ASG GROUP</div>

<p>At ASG Studios & ASG Group (<strong>"ASG," "we," "us," or "our"</strong>), we are committed to protecting your personal information and your right to privacy. This Privacy Policy describes how we collect, use, and share information when you use our website at <strong>asgstudios.online</strong>.</p>

<h2>1. INFORMATION WE COLLECT</h2>
<p>We collect information in the following ways:</p>
<ul>
  <li><strong>Account Registration:</strong> When you create an account, we collect your name, email address, and password (stored as a bcrypt hash).</li>
  <li><strong>OAuth Authentication:</strong> When you log in via Google or GitHub, we receive your profile name, email, and avatar from those providers.</li>
  <li><strong>Purchases & Donations:</strong> We collect billing name, email, and shipping address. Payment processing is handled by Razorpay or PayPal — we do not store card numbers.</li>
  <li><strong>Contact Forms:</strong> Name, email, and message content.</li>
  <li><strong>Automatically Collected:</strong> IP address, browser type, pages visited, and timestamps for security and analytics.</li>
</ul>

<h2>2. HOW WE USE YOUR INFORMATION</h2>
<ul>
  <li>To provide and maintain our services</li>
  <li>To process transactions and send confirmations</li>
  <li>To send newsletters (only with your consent)</li>
  <li>To detect and prevent fraud or abuse</li>
  <li>To improve our platform and user experience</li>
  <li>To comply with legal obligations</li>
</ul>

<h2>3. DATA SHARING</h2>
<p>We do not sell your personal data. We may share it with:</p>
<ul>
  <li><strong>Payment Processors:</strong> Razorpay, PayPal — for transaction processing</li>
  <li><strong>OAuth Providers:</strong> Google, GitHub — for authentication only</li>
  <li><strong>Law Enforcement:</strong> If required by applicable law or court order</li>
</ul>

<h2>4. DATA SECURITY</h2>
<p>We implement military-grade security measures including: bcrypt password hashing (cost factor 12), CSRF protection on all forms, rate limiting, HTTPS enforcement (HSTS), Content Security Policy headers, and regular security audits. Your data is stored on secure servers with restricted access.</p>

<h2>5. YOUR RIGHTS</h2>
<p>You have the right to: access your personal data, correct inaccurate data, delete your account and associated data, opt out of newsletters at any time, and request a copy of your data. Contact us at <a href="mailto:privacy@asgstudios.online">privacy@asgstudios.online</a> to exercise these rights.</p>

<h2>6. COOKIES</h2>
<p>We use strictly necessary session cookies for authentication. We do not use tracking or advertising cookies. See our <a href="/pages/cookies.php">Cookie Policy</a> for details.</p>

<h2>7. DATA RETENTION</h2>
<p>Account data is retained as long as your account is active. Deleted accounts are purged within 30 days. Audit logs are retained for 12 months for security compliance.</p>

<h2>8. CHILDREN'S PRIVACY</h2>
<p>Our services are not directed to children under 13. We do not knowingly collect personal information from children under 13.</p>

<h2>9. CHANGES TO THIS POLICY</h2>
